#include <bits/stdc++.h>
using namespace std;

int main() {
	int n,a;
	float b,m;
	cin>>n>>m>>a>>b;
	
	if (b/m < a) {
		int off_ride = (n/m);
		int remain_ride = n - off_ride*m;
		int mix = off_ride*b+remain_ride*a;
		int full_off = (off_ride+1)*b;
		if (full_off < mix) cout << full_off;
		else cout<<mix;
	}
	else cout<<n*a;
}